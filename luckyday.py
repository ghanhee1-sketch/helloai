import random

# 1. 데이터 저장소 (딕셔너리 문법 공부)
# 기분별로 어울리는 메뉴 리스트를 저장합니다.
menu_box = {
    "신남": ["스테이크", "피자", "마라탕"],
    "우울": ["초콜릿 케이크", "매운 떡볶이", "따뜻한 코코아"],
    "평범": ["비빔밥", "샌드위치", "쌀국수"]
}

# 행운의 아이템 리스트 (리스트 문법 공부)
items = ["파란색 볼펜", "반짝이는 동전", "좋아하는 노래", "노란색 양말"]

print("🔮 오늘의 운세 & 메뉴 추천기에 오신 것을 환영합니다!")

# 2. 사용자 입력 받기
mood = input("오늘의 기분은 어떤가요? (신남/우울/평범): ")

# 3. 조건문과 랜덤의 만남
if mood in menu_box:
    # 해당 기분에 맞는 리스트에서 하나를 무작위로 뽑습니다.
    my_menu = random.choice(menu_box[mood])
    my_item = random.choice(items)
    
    print("\n--- 오늘의 결과 ---")
    print(f"💌 기분이 {mood} 보이고 싶을 땐 '{my_menu}'을(를) 드셔보세요!")
    print(f"✨ 오늘의 행운 아이템은 '{my_item}'입니다.")
else:
    print("❌ '신남, 우울, 평범' 중에서 골라주세요!")
    